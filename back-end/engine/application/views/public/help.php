<h3 id="link501">Где взять товар?</h3>
<p>


Для того чтобы найти товары для продажи достаточно написать в любую оптовую фирму. По нашим сведениям договорится с поставщиками имея только лишь интернет магазин можно более чем в 90% случаев, потому что это в их интересах.
</p><p>
Оплату за товар лучше всего производить по факту реализации, поскольку клиентов на этапе старта обычно не так много, чтобы товар расходился быстро, тем более почувствовать, что будут брать клиенты, не у всех получается с первого раза. Когда магазин вырастает имеет смысл выкупать какой-то объем товаров и создавать свой, небольшой склад. Большинство поставщиков понимает вашу ситуацию и обычно соглашаются работать под реализацию.
</p><p>
Например можно взять журнал «Товары и цены», созвониться с поставщиками, уточнить условия их работы и подходят ли они вам. Там можно довольно быстро найти поставщиков с выгодными условиями.
</p><p>
Можно так же поискать поставщиков в интернете, просто забив запрос в Яндексе «... оптом» и написав тем сайтам, которые находятся в первой сотне результатов. Так вы найдёте оптимальные цены и сможете оценить уровень конкуренции в данной нише. Обычно он не так велик как кажется, поскольку рынок электронной коммерции всё еще находится в зачаточном состоянии, относительно зарубежных аналогов, и потенциал роста, в ближайшие 5-10 лет измеряется тысячами процентов.
</p>

<h3 id="link502">Можно ли открыть интернет магазин без регистрации ООО/ИП?</h3>
<p>Да, конечно же можно. Для запуска интернет магазина не требуется наличие юр.лица. Например если Вы создаёте магазин и основная его задача - привлечь покупателей и заказы через интернет, а непосредственную обработку заказов будет проводить другая фирма, то юр. лицо Вам не понадобится.
</p><p>
По сути интернет магазин не сильно отличается от печатного журнала, или газеты объявлений т.к. он не производит непосредственных рассчётов с покупателем. Регистрация фирмы требуется в том случае, если планируется обрабатывать заказы самостоятельно.
</p><p>
Многие предприниматели открывают магазин, самостоятельно обрабатывают заказы и не регистрируют себе юр.лиц, но это шаткий путь. Всё дело в том что по закону физическое лицо может не регистрировать фирму, если делает лишь одну продажу другому физическому лицу. Если же продажи систематические (две и более), то государство требует оформления юридических документов. В случае с интернет магазинами проверить факт систематической продажи не всегда просто, поэтому многие владельцы магазинов не регистрируются в качестве ИП. Со своей стороны мы советуем, в конечном итоге, оформлять все документы как положено. Это заложит крепкий фундамент для последующей деятельности.
</p>




