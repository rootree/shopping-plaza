<p>Наши специалисты постоянно занимаются доработкой и созданием нового функционала. Для того чтобы они сделали тот или иной модуль, просто добавьте нам задачу на реформале или проголосуйте за существующую. Наш план разработки всегда отталкивается от этого списка приоритетных задач.
