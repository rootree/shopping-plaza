<h3 id="link603">Если магазин блокируется, потеряются ли данные которые так долго в него вносили?</h3>
<p>Данные при блокировке магазина не удаляются. Блокируется только витрина магазина. Вход в бэк-офис доступен всегда по адресу вида domain.ru/admin/.
</p>
<p>
